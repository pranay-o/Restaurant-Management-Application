    public void validateField() {
        String name = txtName.getText();
        String price = txtPrice.getText();
        String category = (String) jComboBox1.getSelectedItem(); // Get the selected item from the "jComboBox1" combo box
        if (!name.equals("") && !price.equals("") && category != null) {
            // If the name, price, and category are not empty or null, enable the "Update" button
            btnUpdate.setEnabled(true);
        } else {
            // Otherwise, disable the "Update" button
            btnUpdate.setEnabled(false);
        }
    }

